            <h2>Definiendo <span>mi sitio</span></h2>
                <font face="Arial, Helvetica, sans-serif" size="2"><b>Una 
              cosa importante: </b>este es el paso más importante: setear,
      dejar bien configurado en qué carpeta vamos a colocar TODO lo que vamos a
      diseñar, y dejar así armado el sitio TAL CUAL lo vamos a tener en el FTP,
      es decir, en el dominio que tengamos ya contratado para tener nuestra
      página online. En resumen, les quiero decir que, si tenemos todo disperso
      por cualquier parte de nuestro rígido, el mismo programa nos va a pedir
      que coloquemos todo en un mismo lado.</font>
      <p align="left"><font size="2" face="Arial, Helvetica, sans-serif">El desarrollo 
        de un sitio Web con Dreamweaver comienza en la definici&oacute;n de una 
        carpeta local para el sitio. En esta carpeta se guardar&aacute;n todos 
        los archivos que lo conformen.<br>
        Debemos hacer una carpeta en nuestro disco para cada sitio que deseemos 
        crear. <br>
        <br>
        El paso siguiente es abrir el programa Dreamweaver. </font></p>
      <p align="left"><font size="2" face="Arial, Helvetica, sans-serif">Abrimos
      el programa y nos encontramos con dos ventanas (si tenemos las
      preferencias por default) una hoja en blanco (listo para diseñar) y otra
      que se denomina Site, es decir nos muestra todo lo que tenemos en nuestra
      raíz. Elegimos Sitio/Nuevo Sitio (Site/New Site). Se abrirá un cuadro de
      diálogo de definición de sitios. En listado Categorías (Categories),
      seleccionamos Información Local (Local Info). En el campo Nombre del
      Sitio (Site Name) escribimos un nombre para nuestro sitio. En Ruta de
      Carpeta Local (Local Root Folder) debemos indicarle haciendo un click en
      la carpeta amarilla, la ruta a la carpeta que predefinimos anteriormente
      para nuestro sitio. </font></p>
      <p align="center"><img src="images/imagen1.gif" tppabs="http://lawebdelprogramador.com/cursos/dream_4/imagenes/prefesite.gif" width="300" height="191" border="0"></p>
      <p align="center">&nbsp;</p>
      <p><font size="2" face="Arial, Helvetica, sans-serif">Una vez que navegamos 
        hasta la carpeta hacemos un clic en Guardar (Save).<br>
        Inmediatamente hacemos clic sobre el bot&oacute;n OK y el programa nos 
        preguntar&aacute; si queremos que el programa haga un cach&eacute; de 
        los archivos existentes para que actualice r&aacute;pidamente los enlaces 
        cuando cambiamos, renombramos o borramos archivos.</font></p>
      <p align="center"><font size="2" face="Arial, Helvetica, sans-serif"><img src="images/imagen2.gif" tppabs="http://lawebdelprogramador.com/cursos/dream_4/imagenes/site.gif" width="300" height="225" border="0"></font></p>
